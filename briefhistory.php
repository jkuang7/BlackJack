<h1>History of Blackjack</h1>
<img src="images/blackjack-history.jpg" class = "image-center" width="771" height="400">
<p class = "body"> Blackjack derived from a game of unknown origins called twenty-one, also known as <em>Vingt-et-Un</em> in France, or <em>Veintiuna</em> in Spain.
  The first reference of this game can be found in a short stories book called <em>Novelas Ejemplares</em> written by Miguel de Cervantes, a Spanish author and gambler.
  The 1601 story mentions of main characters who are proficient cheaters in <em>veintiuna</em>, where the rule of the game is to reach 21 points without exceeding and the ace can be of either value 1 or 11. 
  The game was first spotted in French casinos in the 1700s as a popular game called <em>Chemin de Fer</em>. It was brought to America by French colonists and since then, it has evolved to the modern-day blackjack.
  <br><br>
  The name blackjack, according to a French card historian, Thierry Depaulis, was given by prospectors during the Klondike Gold Rush(1896-99). The term blackjack refers to a mineral from gold and silver deposits, translating into the top bonus for being dealt an ace
  and a 10-point card. This is contrary to the popular belief the name blackjack came from gambling houses that used to offer ten-to-one bonuses for the ace of spades and a black jack hand.
<br><br>
  Blackjack is a game that can be won by counting. This is not true for any other casino card games. Due to this, many people have come up with strategies to increase their chance of winning.
  A player can count the number of occurences and determine whether the deck is in the player's favor or the dealer's favor.
  The player can then use this chance to bet high to win big, or bet low to reduce their losses.
